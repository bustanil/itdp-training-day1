
public class OperatorPerbandingan {

	public static void main(String[] args) {
		int a = 100;
		int b = 20;
		
		boolean hasil;
		
		// sama dengan
		hasil = a == b; // false
		
		// tidak sama dengan
		hasil = a != b; // true
		
		// kurang dari (sama dengan)
		hasil = a < b; // false
		hasil = a <= b; // false
		
		// lebih dari (sama dengan)
		hasil = a > b; // true
		hasil = a >= b; // true

	}

}
