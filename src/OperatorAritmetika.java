
public class OperatorAritmetika {

	public static void main(String[] args) {
		// penjumlahan
		int jumlahApel = 40;
		int jumlahJeruk = 20;
		int jumlahBuah = jumlahApel + jumlahJeruk;
		
		// pengurangan
		double pendapatan = 10000000.00;
		double biaya = 100000;
		double keuntungan = pendapatan - biaya;
		
		// perkalian
		double totalHarga = 200000;
		double pajak = 0.1 * totalHarga;
		
		// pembagian
		double bungaEfektif = 24;
		double bungaPerBulan = bungaEfektif / 12;
		
		// modulus (sisa pembagian)
		int sisaPembagian = 10 % 4;
		
		// increment
		int i = 2;
		i++; // i = i + 1
		
		// decrement
		int j = 10;
		j--; // j = j - 1
		
		// negatif
		int nilaiAwal = 90;
		int nilaiAkhir = -nilaiAwal; // -90
		
		// positif
		int temperatur = +90;

		// gabungan
		int hasil = 2 - 1 * 4 / 7 % 2 + 9;
		
	}

}
